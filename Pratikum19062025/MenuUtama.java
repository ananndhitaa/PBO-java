    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed

        Double totMakanan= Double.parseDouble(txtMakanan.getText());
        Double totMinuman= Double.parseDouble((txtTotMinuman.getText()));
        Double Total = totMakanan + totMinuman;
        Double pajak = Total*0.1;
        Double bayar = Total + pajak;
        txtTotHarga.setText(Double.toString(Total));
        txtPajak.setText(Double.toString(pajak));
        txtTotBayar.setText(Double.toString(bayar));
        
        txtUang.requestFocusInWindow();
    }//GEN-LAST:event_btnTotalActionPerformed

