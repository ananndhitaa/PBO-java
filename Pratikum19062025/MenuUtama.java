    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        String nama = txtNama.getText();
        String meja = txtMeja.getText();
        
        if(nama.isEmpty() || meja.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Harap isi nama dan nomor meja terlebih dahulu", "Peringatan",JOptionPane.WARNING_MESSAGE);
        return;
    }
        String message = "Nama: " + nama + "\nNomor Meja: " + meja + "\nSelesai Terimakasih!";
        JOptionPane.showMessageDialog(this,message, "Pesanan Selesai", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSelesaiActionPerformed

