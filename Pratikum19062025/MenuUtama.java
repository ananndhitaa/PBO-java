    private void btnHitungKemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungKemActionPerformed
       try{
        double uang = Double.parseDouble(txtUang.getText());
        double totalBayar = Double.parseDouble(txtTotBayar.getText());
            if(uang<totalBayar){
                JOptionPane.showMessageDialog(this, "Uang pembayaran Kurang","WARNING",JOptionPane.INFORMATION_MESSAGE);  
            }
            else{
                Double kembalian = uang - totalBayar;
                txtKembalian.setText(Double.toString(kembalian));
    }
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(this, "Masukan angka yang valid","Uang Pembayaran Error", JOptionPane.ERROR_MESSAGE);
       }
       btnSelesai.requestFocusInWindow();
       
    }//GEN-LAST:event_btnHitungKemActionPerformed

